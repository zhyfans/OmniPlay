    public static string? NormalizeNullable(string? value)
    {
        if (string.IsNullOrWhiteSpace(value))
        {
            return value;
        }

        return Normalize(value);
    }

    public static string NormalizeTitle(string title)
    {
        return Normalize(title).Trim();
    }

    public static string Normalize(string value)
    {
        if (string.IsNullOrEmpty(value))
        {
            return value;
        }

        var normalized = value;
        foreach (var (traditional, simplified) in PhraseMap)
        {
            normalized = normalized.Replace(traditional, simplified, StringComparison.Ordinal);
        }

        var builder = new StringBuilder(normalized.Length);
        foreach (var character in normalized)
        {
            builder.Append(CharacterMap.TryGetValue(character, out var replacement) ? replacement : character);
        }

        normalized = builder.ToString();
        foreach (var (traditional, simplified) in PhraseMap)
        {
            normalized = normalized.Replace(traditional, simplified, StringComparison.Ordinal);
        }

        return normalized;
    }
